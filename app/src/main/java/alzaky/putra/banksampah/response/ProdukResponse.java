package alzaky.putra.banksampah.response;

public class ProdukResponse {
    private int id;
    private String nama;
    private int harga;
    private int tipe;
    private String deskripsi;
    private String gambar;

    public int getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public int getHarga() {
        return harga;
    }

    public int getTipe() {
        return tipe;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public String getGambar() {
        return gambar;
    }
}